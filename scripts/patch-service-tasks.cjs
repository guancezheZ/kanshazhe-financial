function genTaskCode(t) {
  const tags = t.g.map(x => "'" + x + "'").join(',')
  const role = t.r ? "role: '" + t.r + "', " : ''
  if (!t.es || t.es.length === 0) {
    return "  { date: '" + t.d + "', title: '" + t.t + "', tags: [" + tags + "], difficulty: 1, " + role + "entries: [], documents: [{ type: 'text', label: '记录' }] },"
  }
  const es = t.es.map(e => {
    if (e.x) return "{subjectCode:'" + e.s + "',debit:" + e.x + ",credit:0}"
    return "{subjectCode:'" + e.s + "',debit:0,credit:" + e.c + "}"
  }).join(',')
  return "  { date: '" + t.d + "', title: '" + t.t + "', tags: [" + tags + "], difficulty: 1, " + role + "entries: [" + es + "], documents: [{ type: 'text', label: '记录' }] },"
}

