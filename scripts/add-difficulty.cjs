function processFile(filePath, label, month) {
  let content = fs.readFileSync(filePath, 'utf8')
  const map = DIFFICULTY[label] || DIFFICULTY[month]
  if (!map) { console.error(`No mapping for ${label}`); return }

  let count = 0

  // Match each task object: find date and title, then look for the tags line
  // Pattern: find a block starting with `date: '2026-...'` followed by `title: '...'`
  // Then insert difficulty: X after the tags line
  for (const [key, diff] of Object.entries(map)) {
    const [date, title] = key.split('|')
    // Build a regex that finds this specific task and adds difficulty after tags
    // We need to be careful about escaping special chars in title
    const escapedTitle = title.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const regex = new RegExp(
      `(date:\\s*'${date}'\\s*,\\s*\\n\\s*title:\\s*'${escapedTitle}'[\\s\\S]*?tags:\\s*\\[[^\\]]*\\])`,
      'm'
    )
    const match = content.match(regex)
    if (match) {
      const replacement = match[1] + `,\n      difficulty: ${diff}`
      content = content.replace(match[1], replacement)
      count++
    } else {
      console.warn(`⚠ Not found: [${label}] ${date} — ${title}`)
    }
  }

  fs.writeFileSync(filePath, content, 'utf8')
  console.log(`✅ ${label}: ${count}/${Object.keys(map).length} tasks updated`)
}

