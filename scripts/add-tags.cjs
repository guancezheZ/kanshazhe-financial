function addTagsToFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf-8')
  let modified = false
  const lines = content.split('\n')
  const result = []

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i]
    result.push(line)

    // 匹配 title: 'xxx', 行（支持任意缩进）
    const titleMatch = line.match(/^(\s+)title:\s+'([^']+)',$/)
    if (titleMatch) {
      const indent = titleMatch[1]
      const title = titleMatch[2]
      const tags = TAG_MAP[title]
      if (tags) {
        const nextLine = lines[i + 1] || ''
        // 如果下一行已经有 tags，替换之
        if (nextLine.trim().startsWith('tags:')) {
          // 替换下一行（当前行已 push，现在 pop 下一行再推新 tags）
          result.push(`${indent}tags: ${JSON.stringify(tags)},`)
          i++
          modified = true
        } else {
          // 插入新行
          result.push(`${indent}tags: ${JSON.stringify(tags)},`)
          modified = true
        }
      }
    }
  }

  if (modified) {
    fs.writeFileSync(filePath, result.join('\n'))
    console.log(`✅ 已更新: ${path.relative(process.cwd(), filePath)}`)
  } else {
    console.log(`ℹ️  无变化: ${path.relative(process.cwd(), filePath)}`)
  }
}

