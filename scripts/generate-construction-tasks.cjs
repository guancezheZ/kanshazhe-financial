function jsStr(s) {
  return `'${s.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\n/g, '\\n')}'`;
}

function jsVal(v, indent) {
  if (v === null || v === undefined) return 'null';
  if (typeof v === 'string') return jsStr(v);
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return String(v);
  if (Array.isArray(v)) {
    if (v.length === 0) return '[]';
    const pad = '  '.repeat(indent + 1);
    const items = v.map(item => pad + jsVal(item, indent + 1));
    return '[\n' + items.join(',\n') + '\n' + '  '.repeat(indent) + ']';
  }
  if (typeof v === 'object') {
    const keys = Object.keys(v);
    if (keys.length === 0) return '{}';
    const pad = '  '.repeat(indent + 1);
    const entries = keys.map(k => {
      const val = jsVal(v[k], indent + 1);
      return `${pad}${k}: ${val}`;
    });
    return '{\n' + entries.join(',\n') + '\n' + '  '.repeat(indent) + '}';
  }
  return String(v);
}

function generateMonthFile(month, tasks) {
  const m = parseInt(month);
  let subtitle = '';
  const subtitles = {
    1: '企业设立与首个项目中标',
    2: '基础施工推进与分包进场',
    3: '主体结构施工与材料采购',
    4: '主体施工推进与成本控制',
    5: '新项目启动与双项目并行',
    6: '半年进度冲刺与质保金处理',
    7: '设计变更与签证索赔',
    8: '高温雨季施工与成本管控',
    9: '首个项目竣工决算',
    10: '竣工审计与新项目承接',
    11: '年末盘点与进度款催收',
    12: '年终决算与利润分配',
  };
  subtitle = subtitles[m] || '';

  const tasksStr = tasks.map(t => '  ' + jsVal(t, 1)).join(',\n');

  return `/**
 * 建筑业 - ${m}月教学任务
 *
 * 企业：鼎立建筑工程有限公司
 * 税制：一般纳税人（增值税9%）
 * 准则：CAS 14 新收入准则（投入法/完工百分比）
 * 本月主题：${subtitle}
 */

const tasks = [
${tasksStr}
]

export default tasks
`;
}

function writeMonthFile(month, tasks) {
  const filePath = path.join(CONSTRUCTION_DIR, `${month}.js`);
  const bakPath = filePath + '.bak';

  if (fs.existsSync(filePath) && !fs.existsSync(bakPath)) {
    fs.copyFileSync(filePath, bakPath);
    console.log(`  ✅ 已备份 ${month}.js → ${month}.js.bak`);
  }

  const content = generateMonthFile(month, tasks);
  fs.writeFileSync(filePath, content, 'utf8');
  const lines = content.split('\n').length;
  console.log(`  ✅ 已写入 ${month}.js（${tasks.length} 个任务，${lines} 行）`);
}

/**
 * 任务辅助函数（智能参数处理）
 * t(date, title, tags, difficulty, desc, tip, entries, [documents], [nextAction])
 * 兼容：entries之后可以是 documents数组 + nextAction字符串，或直接 nextAction字符串 + documents数组
 */
function t(date, title, tags, difficulty, description, tip, entries, ...rest) {
  let documents = [];
  let nextAction = null;
  if (rest.length >= 2) {
    // 两个额外参数: [documents, nextAction]
    documents = Array.isArray(rest[0]) ? rest[0] : (Array.isArray(rest[1]) ? rest[1] : []);
    nextAction = typeof rest[0] === 'string' ? rest[0] : (typeof rest[1] === 'string' ? rest[1] : null);
  } else if (rest.length === 1) {
    // 一个额外参数
    if (typeof rest[0] === 'string') nextAction = rest[0];
    else if (Array.isArray(rest[0])) documents = rest[0];
  }
  const task = { date, role: 'accountant', title, tags, difficulty, description, tip, entries, documents };
  if (nextAction) task.nextAction = nextAction;
  return task;
}

function e(subjectCode, debit, credit, summary, explanation, cashFlowItem, cashFlowExplanation) {
  const entry = { subjectCode, debit, credit, summary, explanation };
  if (cashFlowItem) entry.cashFlowItem = cashFlowItem;
  if (cashFlowExplanation) entry.cashFlowExplanation = cashFlowExplanation;
  return entry;
}

